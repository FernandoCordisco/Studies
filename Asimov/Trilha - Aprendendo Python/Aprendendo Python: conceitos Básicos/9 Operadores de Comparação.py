print('Comparação com o número 10')
numero = int(input('Digite um número: '))

if numero == 10:
    print(f'{numero} é igual a 10.')
    
elif numero != 10:
    print(f'{numero} é diferente de 10.')

    if numero > 10:
        print(f'{numero} é maior do que 10')

    else:
        print(f'{numero} é menor do que 10')

